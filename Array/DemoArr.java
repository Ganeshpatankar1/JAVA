public class DemoArr {
    public static void main(String[] args) {
        int[] arr = new int[6];
        arr[2] = 45;
        System.out.println(arr[2]);

        int[] marks = {34,53,534,53,53};
        for (int i : marks) {
            System.out.println(i);
        }
        
    }
}
